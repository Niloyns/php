<!-- PHP code to print a message -->
<?php
    echo "Hello Worlds"; // Prints the message "Hello Worlds"
?>

<!-- We can use PHP in HTML -->
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>PHP in HTML Example</title>
</head>
<body>
    <!-- PHP code embedded within HTML -->
    <?php
        echo "Hello Everyone"; // Prints the message "Hello Everyone" within the body of the HTML document
    ?>
</body>
</html>
